#!/usr/bin/env python3
"""Generate sass/moe_w4_mm.sass — M<=4 GEMM over FULL-FP4 (e2m1) planes.

Delta-tier sibling of moe_w4_mm: identical ABI/desc/epilogue, B source is
the full e2m1 nibble plane (N*K/2 bytes, fragment-major words per k32 per
tile). Decode: magnitude via PRMT over the 8-entry e4m3 LUT pool
{0,.5,1,1.5,2,3,4,6}, sign via IMAD-spread of nibble bit3 -> byte MSB OR.

C[M,N] (bf16) = A(fp8 e4m3, [M,K]) @ W(2-bit codes {-4,-1,1,4} fragment-
major plane, block-32 UE8M0 scales) ^T, with per-token-group-128 f32
activation scales folded per kb128 (the proven skinny fold structure).

Differences vs gen_skinny_fp8 (ABI2 fold):
  - B source: 2-bit plane (N*K/4 bytes). Per lane per k64: LDG.E.64 of the
    8-byte fragment chunk -> PRMT-LUT decode to e4m3 bytes on the INT pipe.
  - B scales: block-32 UE8M0 bytes feed QMMA.SF's sfb DIRECTLY per k32
    per TILE (sfb scales the lane's QMMA column = one W row; tile0/tile1
    cover different rows -> separate bytes). sfa stays neutral; the A f32
    scale folds per kb128.
  - Per-expert indirection: d_b/d_bs/d_a/d_as/d_c base pointers are read
    from a DESC TABLE indexed by blockIdx.y (one launch covers all
    (expert, token-group) pairs of a layer): desc = 6 x u64 per pair
    {a, as, b, bs, c, m_rows}. K/N are compile-time per cubin.
  - Scales plane layout (packer pack_scales): sbyte[nb, ks, row16] at
    offset (nb*(K/32) + ks)*16 + r, r = row inside the 16-row block.
    Lane (g,t) loads r=g (tile0) and r=8+g (tile1) per k32: 4x LDG.E.U8
    per k64, L1-hot (64B/k64 shared by the whole CTA).

Geometry: grid.x = N/16, grid.y = num pairs; NWARP=8 split-K (the
acceptance-parity-proven structure), smem reduce, bf16 epilogue --
all inherited verbatim from skinny.
"""

import os
import sys

K = int(sys.argv[2]) if len(sys.argv) > 2 else 4096


def _nwarp(k):
    nb = k // 128
    cap = 8 if k >= 1024 else 4
    for n in range(min(cap, nb), 0, -1):
        if nb % n == 0:
            return n
    return 1


NWARP = int(os.environ.get("MOEW4_NWARP", "0")) or _nwarp(K)
KSLICE = K // NWARP
assert KSLICE % 128 == 0
NTHR = NWARP * 32
SMEM = NWARP * 1024 + (2048 if NTHR > 256 else 0)
ACC0, ACC1 = 48, 52

OUT = []


def L(s=""):
    OUT.append(s)


L("// moe_w4_mm — M<=4 GEMM over 2-bit tensor-sym planes (GENERATED by")
L("// tools/gen_moe_w2.py — edit the generator, not this file).")
L("// PRMT-LUT decode {-4,-1,1,4} -> e4m3; block-32 UE8M0 via QMMA.SF sfb;")
L("// A-side f32 scales folded per kb128; per-(expert,token-group) desc")
L("// table indexed by CTAID.Y. Skinny-fold lineage (multi-CTA safe).")
L(".entry moe_w4_mm")
L("    .reg R0-R95")
L("    .param u64 d_desc")        # [pairs][6] u64 table
L("    .param u32 ldb")           # K (bytes per A row; codes row = K/4)
L("    .param u32 n_k64")
L("    .param u32 ldc")           # N*2
L("    .param u32 n_kb128")
L(f"    .shared .align 16 smem[{SMEM}]")
L()
L("    // ---- prologue ----")
L("    S2R R80, SR_TID.X ;")
L("    LDCU.64 UR4, c[0x0][0x358] ;")
L("    UMOV UR6, 0x0 ;")
L("    S2R R81, SR_CTAID.X ;                    // n-block (16 rows)")
L("    S2R R92, SR_CTAID.Y ;                    // (expert, token-group) pair")
L("    LDC R82, c[0x0][0x388] ;                 // ldb = K")
L("    LDC R84, c[0x0][0x394] ;                 // n_kb128")
L("    SHF.R.U32.HI R85, RZ, 0x5, R80 ;         // wid")
L("    LOP3.LUT R86, R80, 0x1f, RZ, 0xc0, !PT ; // lane")
L("    SHF.R.U32.HI R87, RZ, 0x2, R86 ;         // g")
L("    LOP3.LUT R88, R86, 0x3, RZ, 0xc0, !PT ;  // t")
L("    SHF.L.U32 R89, R88, 0x2, RZ ;            // 4t")
L()
L("    // desc = d_desc + pair*48 ; fields: a, as, b(codes), bs, c, m_rows")
L("    LDC.64 R2, c[0x0][0x380] ;               // d_desc")
L("    IMAD R0, R92, 0x30, RZ ;")
L("    IMAD.WIDE.U32 R2, R0, 0x1, R2 ;")
L("    LDG.E.128 R4, desc[UR4][R2.64] ;         // a_lo,a_hi,as_lo,as_hi")
L("    LDG.E.128 R8, desc[UR4][R2.64+0x10] ;    // b_lo,b_hi,bs_lo,bs_hi")
L("    LDG.E.128 R12, desc[UR4][R2.64+0x20] ;   // c_lo,c_hi,m,pad")
L("    MOV R64, R4 ; MOV R65, R5 ;              // d_a")
L("    MOV R66, R6 ; MOV R67, R7 ;              // d_as")
L("    MOV R68, R8 ; MOV R69, R9 ;              // d_b (2-bit plane)")
L("    MOV R74, R10 ; MOV R75, R11 ;            // d_bs (scale plane)")
L("    MOV R76, R12 ; MOV R77, R13 ;            // d_c")
L("    MOV R79, R14 ;                           // m_rows")
L("    // pad pair (m_rows==0, moe_align filler): leave before any plane I/O")
L("    ISETP.EQ.AND P4, PT, R79, RZ, PT ;")
L("    @P4 EXIT ;")
L("    LDC R78, c[0x0][0x390] ;                 // ldc")
L()
L("    // P2: g < m_rows (A loads); P3: t==0 && P2 (A-scale owner)")
L("    ISETP.LT.AND P2, PT, R87, R79, PT ;")
L("    ISETP.EQ.AND P3, PT, R88, RZ, P2 ;")
L()
L(f"    // B plane ptr: (ctaid.x*(K/64) + wid*(KSLICE/64)) blocks of 256B,")
L("    // + lane*8 inside the block")
L("    LDC R83, c[0x0][0x38c] ;                 // n_k64 = K/64")
L("    IMAD R0, R81, R83, RZ ;                  // nb * n_k64")
L(f"    IMAD R0, R85, {hex(KSLICE // 64)}, R0 ;  // + wid slice base (k64 units)")
L("    SHF.L.U32 R0, R0, 0x9, RZ ;              // *512B per (nb,k64,32 lanes)")
L("    IMAD.WIDE.U32 R8, R0, 0x1, R68 ;")
L("    SHF.L.U32 R1, R86, 0x4, RZ ;             // lane*16")
L("    IMAD.WIDE.U32 R8, R1, 0x1, R8 ;          // -> R8: lane's plane ptr")
L()
L("    // Bs plane ptr: ((nb*(K/32) + wid*(KSLICE/32))*16 + g  [16 row")
L("    // bytes per k32; lane reads row g (tile0) and row 8+g (tile1)]")
L("    SHF.R.U32.HI R0, RZ, 0x5, R82 ;          // K/32")
L("    IMAD R1, R81, R0, RZ ;")
L(f"    IMAD R1, R85, {hex(KSLICE // 32)}, R1 ;")
L("    SHF.L.U32 R1, R1, 0x4, RZ ;              // *16")
L("    IADD3 R1, PT, PT, R1, R87, RZ ;          // + g")
L("    IMAD.WIDE.U32 R74, R1, 0x1, R74 ;        // -> R74: lane's sbyte ptr")
L()
L("    // A row ptr (row g, direct fp8) + As ptr (f32 [M, K/128])")
L("    IMAD.WIDE.U32 R64, R87, R82, R64 ;       // + g*K")
L("    IMAD.WIDE.U32 R64, R89, 0x1, R64 ;       // + 4t")
L(f"    IMAD R0, R85, {hex(KSLICE)}, RZ ;")
L("    IMAD.WIDE.U32 R64, R0, 0x1, R64 ;        // + wid K-slice")
L("    SHF.L.U32 R0, R84, 0x2, RZ ;             // As row stride = n_kb128*4")
L("    IMAD.WIDE.U32 R66, R87, R0, R66 ;        // + g*stride")
L(f"    IMAD.WIDE.U32 R66, R85, {hex((KSLICE // 128) * 4)}, R66 ;")
L()
L("    // constants: pad A words zero, LUT, neutral sfa, kb accs zero")
L("    MOV R28, 0x7f7f7f7f ;                    // sfa neutral")
for r in (5, 7, 21, 23, 4, 6, 20, 22, 30, 31, 40, 41, 42, 43, 44, 45, 46, 47):
    L(f"    MOV R{r}, RZ ;")
L("    MOV R27, 0x3c383000 ;                    // e2m1 mag LUT lo: 0,.5,1,1.5")
L("    MOV R58, 0x4c484440 ;                    // e2m1 mag LUT hi: 2,3,4,6")
for r in list(range(ACC0, ACC0 + 4)) + list(range(ACC1, ACC1 + 4)):
    L(f"    MOV R{r}, RZ ;")
L()


def emit_k64(j):
    """One k64: load 8B codes + 4 sbytes + A bytes; decode; 4x QMMA; fold/2."""
    plane_off = 0x200 * j           # 512B per k64 block (32 lanes x 16B)
    aoff = 0x40 * j
    soff = 0x20 * j                 # 2 k32 x 16 row bytes per k64
    kb = j // 2
    L(f"    // ---- k64 {j} (kb128 {kb}) ----")
    if j % 2 == 0:
        L(f"    @P2 LDG.E R30, desc[UR4][R66.64+{hex(kb * 4)}] ;  // As[g,kb] f32")
    # 2-bit fragment chunk: R24 = bytes [t0a_lo, t0a_hi, t0b_lo, t0b_hi],
    #                       R25 = tile1 likewise
    L(f"    LDG.E.128 R12, desc[UR4][R8.64+{hex(plane_off)}] ;    // nibbles 16B")
    L(f"    LDG.E.U8 R26, desc[UR4][R74.64+{hex(soff)}] ;         // S[g, k32a]")
    L(f"    LDG.E.U8 R29, desc[UR4][R74.64+{hex(soff + 0x8)}] ;   // S[8+g, k32a]")
    L(f"    LDG.E.U8 R56, desc[UR4][R74.64+{hex(soff + 0x10)}] ;  // S[g, k32b]")
    L(f"    LDG.E.U8 R57, desc[UR4][R74.64+{hex(soff + 0x18)}] ;  // S[8+g, k32b]")
    L(f"    @P2 LDG.E R4, desc[UR4][R64.64+{hex(aoff)}] ;         // A w0 lo")
    L(f"    @P2 LDG.E R6, desc[UR4][R64.64+{hex(aoff + 0x10)}] ;  // A w2")
    L(f"    @P2 LDG.E R20, desc[UR4][R64.64+{hex(aoff + 0x20)}] ;")
    L(f"    @P2 LDG.E R22, desc[UR4][R64.64+{hex(aoff + 0x30)}] ;")
    L("    // FP4 word decode: mag PRMT (pool R27:R58) + sign spread OR")
    # word w (8 nibbles = one k32 fragment of one tile): R24=t0a R25=t0b
    # R26->? NO: R24..R27 hold the 4 words; R26/R29/R56/R57 are sbytes ->
    # words land in R24,R25,R16,R17 via the 128 load? LDG.E.128 R24 fills
    # R24,R25,R26,R27 -- COLLIDES with sbytes(R26)/LUT(R27)! Load into
    # R12..R15 instead (desc regs are dead after the prologue).
    for w, (lo, hi) in (("R12", (32, 33)), ("R13", (34, 35)),
                        ("R14", (36, 37)), ("R15", (38, 39))):
        for half, dst in ((0, lo), (1, hi)):
            if half == 0:
                L(f"    LOP3.LUT R1, {w}, 0x7777, RZ, 0xc0, !PT ;   // mag sels")
                L(f"    LOP3.LUT R2, {w}, 0x8888, RZ, 0xc0, !PT ;   // sign bits")
            else:
                L(f"    SHF.R.U32.HI R1, RZ, 0x10, {w} ;")
                L(f"    SHF.R.U32.HI R2, RZ, 0x10, {w} ;")
                L("    LOP3.LUT R1, R1, 0x7777, RZ, 0xc0, !PT ;")
                L("    LOP3.LUT R2, R2, 0x8888, RZ, 0xc0, !PT ;")
            L(f"    PRMT R{dst}, R27, R1, R58 ;              // 4x mag bytes")
            L("    LOP3.LUT R0, R2, 0x8, RZ, 0xc0, !PT ;")
            L("    LOP3.LUT R3, R2, 0x80, RZ, 0xc0, !PT ;")
            L("    IMAD R0, R0, 0x10, RZ ;                  // bit3 -> bit7")
            L("    IMAD R0, R3, 0x100, R0 ;                 // bit7 -> bit15")
            L("    LOP3.LUT R3, R2, 0x800, RZ, 0xc0, !PT ;")
            L("    IMAD R0, R3, 0x1000, R0 ;")
            L("    LOP3.LUT R3, R2, 0x8000, RZ, 0xc0, !PT ;")
            L("    IMAD R0, R3, 0x10000, R0 ;")
            L(f"    LOP3.LUT R{dst}, R{dst}, R0, RZ, 0xfc, !PT ; // OR signs")
    L("    // QMMA per k32; sfb = the lane's COLUMN row scale (tile-exact)")
    L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R40, R4.ROW, R32.COL, R40, R28, R26, UR6 ;")
    L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R44, R4.ROW, R36.COL, R44, R28, R29, UR6 ;")
    L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R40, R20.ROW, R34.COL, R40, R28, R56, UR6 ;")
    L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R44, R20.ROW, R38.COL, R44, R28, R57, UR6 ;")
    if j % 2 == 1:
        L(f"    // fold kb128 {kb}: master += acc_kb * As (B scale already in SF)")
        L("    FFMA R48, R40, R30, R48 ;")
        L("    FFMA R49, R41, R30, R49 ;")
        L("    FFMA R52, R44, R30, R52 ;")
        L("    FFMA R53, R45, R30, R53 ;")
        L("    MOV R40, RZ ; MOV R41, RZ ;")
        L("    MOV R44, RZ ; MOV R45, RZ ;")


n_k64 = KSLICE // 64
L(f"    // ---- K march: {n_k64} x k64 ----")
for j in range(n_k64):
    emit_k64(j)
L()
L("    // ---- epilogue: smem split-K reduce + bf16 store (skinny lineage) ----")
L("    IMAD R0, R85, 0x400, RZ ;")
L("    SHF.L.U32 R1, R86, 0x4, RZ ;")
L("    IADD3 R0, PT, PT, R0, R1, RZ ;")
for i in range(4):
    L(f"    STS [R0+{hex(i * 4)}], R{ACC0 + i} ;")
for i in range(4):
    L(f"    STS [R0+{hex(0x200 + i * 4)}], R{ACC1 + i} ;")
L("    BAR.SYNC.DEFER_BLOCKING 0x0 ;")
L()


def emit_reduce_store(slot_base):
    sb = f"+{hex(slot_base)}" if slot_base else ""
    L(f"    // slot = tid{sb}")
    if slot_base:
        L(f"    IADD3 R19, PT, PT, R80, {hex(slot_base)}, RZ ;")
    else:
        L("    MOV R19, R80 ;")
    L("    LOP3.LUT R2, R19, 0x7f, RZ, 0xc0, !PT ;")
    L("    SHF.R.U32.HI R3, RZ, 0x7, R19 ;")
    L("    IMAD R4, R3, 0x200, RZ ;")
    L("    IMAD R4, R2, 0x4, R4 ;")
    L("    LDS R30, [R4] ;")
    for wid in range(1, NWARP):
        L(f"    IADD3 R5, PT, PT, R4, {hex(wid * 0x400)}, RZ ;")
        L("    LDS R21, [R5] ;")
        L("    FADD R30, R30, R21 ;")
    L("    SHF.R.U32.HI R20, RZ, 0x2, R2 ;")
    L("    LOP3.LUT R21, R2, 0x3, RZ, 0xc0, !PT ;")
    L("    SHF.R.U32.HI R22, RZ, 0x2, R20 ;")
    L("    SHF.R.U32.HI R23, RZ, 0x1, R21 ;")
    L("    IMAD R22, R23, 0x8, R22 ;")
    L("    LOP3.LUT R24, R20, 0x3, RZ, 0xc0, !PT ;")
    L("    LOP3.LUT R25, R21, 0x1, RZ, 0xc0, !PT ;")
    L("    IMAD R24, R24, 0x2, R25 ;")
    L("    SHF.L.U32 R26, R81, 0x4, RZ ;")
    L("    IMAD R26, R3, 0x8, R26 ;")
    L("    IADD3 R26, PT, PT, R26, R24, RZ ;")
    L("    SHFL.DOWN PT, R31, R30, 0x1, 0x1f ;")
    L("    F2FP.BF16.F32.PACK_AB R31, R31, R30 ;")
    L("    ISETP.LT.AND P1, PT, R22, R79, PT ;")
    L("    ISETP.EQ.AND P1, PT, R25, RZ, P1 ;")
    if NTHR > 256:
        L("    ISETP.LT.U32.AND P1, PT, R19, 0x100, P1 ;")
    L("    IMAD R14, R22, R78, RZ ;")
    L("    IMAD R14, R26, 0x2, R14 ;")
    L("    MOV R15, RZ ;")
    L("    IADD.64 R16, R14, R76 ;")
    L("    @P1 STG.E desc[UR4][R16.64], R31 ;")


for base in range(0, 256, min(NTHR, 256)):
    emit_reduce_store(base)
L("    EXIT ;")

path = sys.argv[1] if len(sys.argv) > 1 else "/workspace/cubit/sass/moe_w4_mm.sass"
open(path, "w").write("\n".join(OUT) + "\n")
print(f"wrote {path} ({len(OUT)} lines, K={K}, NWARP={NWARP})")
