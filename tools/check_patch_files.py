#!/usr/bin/env python3
"""Guard against patch regenerations that silently drop a line of work.

The distribution patch (patch/vllm-moet-v0.24.0.patch) is a GENERATED
artifact: `git diff v0.24.0 HEAD` from the vllm fork branch. Twice now a
feature line landed in the patch without its source commits reaching the
fork branch, and the next regeneration-from-branch silently erased it
(caught by hand both times — Kimi-K2.7, then the DSpark backport).

This check makes that failure loud: patch/FILES.txt is the committed,
sorted list of files the patch is expected to touch. CI fails when the
patch and the list disagree IN EITHER DIRECTION, so a regeneration that
loses files cannot land without an explicit, reviewable FILES.txt edit.

Regeneration procedure (also in the header of FILES.txt):
    cd <vllm fork>          # the moet-v0.24.0 branch IS the source of truth
    git diff v0.24.0 HEAD > <vLLM-Moet>/patch/vllm-moet-v0.24.0.patch
    python3 tools/check_patch_files.py --update   # then REVIEW the diff:
    # entries VANISHING from FILES.txt = someone's line of work is not in
    # the fork branch — merge it there first, never ship the loss.

Usage: python3 tools/check_patch_files.py [--update]
"""
import os
import re
import sys

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
PATCH = os.path.join(ROOT, "patch", "vllm-moet-v0.24.0.patch")
LIST = os.path.join(ROOT, "patch", "FILES.txt")

HEADER = """\
# Files touched by patch/vllm-moet-v0.24.0.patch (sorted; generated by
# tools/check_patch_files.py --update, verified by CI bench-lint).
# A file DISAPPEARING from this list means a line of work exists in the
# patch but not in the vllm fork branch the patch is generated from —
# merge it into the branch first; do not ship the regeneration.
"""


def patch_files() -> list[str]:
    files = set()
    with open(PATCH) as f:
        for line in f:
            m = re.match(r"^diff --git a/(\S+) b/(\S+)", line)
            if m:
                files.add(m.group(2))
    return sorted(files)


def listed_files() -> list[str]:
    if not os.path.exists(LIST):
        return []
    with open(LIST) as f:
        return sorted(l.strip() for l in f
                      if l.strip() and not l.startswith("#"))


def main() -> int:
    actual = patch_files()
    if "--update" in sys.argv:
        with open(LIST, "w") as f:
            f.write(HEADER)
            f.write("\n".join(actual) + "\n")
        print(f"FILES.txt updated: {len(actual)} files")
        return 0
    expected = listed_files()
    missing = sorted(set(expected) - set(actual))
    extra = sorted(set(actual) - set(expected))
    if not missing and not extra:
        print(f"patch file list OK ({len(actual)} files)")
        return 0
    if missing:
        print("FILES IN FILES.txt BUT MISSING FROM THE PATCH — a line of "
              "work was dropped by a regeneration (merge it into the vllm "
              "fork branch, do not update the list to paper over it):")
        for f in missing:
            print(f"  - {f}")
    if extra:
        print("files in the patch but not in FILES.txt (new work — run "
              "--update and commit the list together with the patch):")
        for f in extra:
            print(f"  + {f}")
    return 1


if __name__ == "__main__":
    sys.exit(main())
