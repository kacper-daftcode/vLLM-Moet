#!/usr/bin/env python3
"""Render bench/results/<release>/ into (a) the generated block in README.md
and (b) docs/benchmarks/<release>.md (the full per-release report).

  render.py                  # render current_release from matrix.yaml
  render.py --release <id>
  render.py --check          # exit 1 if README/report differ from a fresh
                             # render (CI guard: numbers never drift from data)

The README block lives between the markers below; everything inside is
machine-owned. Hand-edits get overwritten (and flagged by --check)."""

import argparse
import os
import sys

sys.path.insert(0, __file__.rsplit("/", 1)[0])

import common

BEGIN = "<!-- bench:table:begin (generated by bench/runner/render.py - do not edit) -->"
END = "<!-- bench:table:end -->"

README = os.path.join(common.REPO_DIR, "README.md")


def _fmt_tok(v):
    if v is None:
        return "—"
    return f"{v:,.0f}".replace(",", " ") if v >= 1000 else f"{v:g}"


def _needle_cell(p):
    if not p or not p.get("cases"):
        return "—"
    passed = [c.get("prompt_tokens") or 0 for c in p["cases"] if c.get("pass")]
    failed = [c for c in p["cases"] if not c.get("pass")]
    cell = f"PASS ≤{max(passed) // 1000}K tok" if passed else ""
    if failed:
        worst = min((c.get("prompt_tokens") or c.get("words") or 0)
                    for c in failed)
        cell += f" ⚠FAIL@{worst // 1000}K"
    return cell or "—"


def _batch_cell(p):
    if not p or not p.get("levels"):
        return "—"
    n = max(p["levels"], key=int)
    lv = p["levels"][n]
    return f"{_fmt_tok(lv.get('aggregate_tok_s'))} @{n}"


def _spec_cell(res):
    sm = res.get("server_metrics") or {}
    acc = sm.get("spec_acceptance_length")
    return f"acc {acc:g}" if acc else ""


def _hw_and_desc(res):
    s = res.get("summary") or ""
    if " - " in s:
        hw, desc = s.split(" - ", 1)
    else:
        hw, desc = s, ""
    return hw or res.get("box", ""), desc


def _decode_cell(res):
    p = (res.get("probes") or {}).get("decode") or {}
    med = p.get("median_tok_s")
    if med is None:
        return "—"
    cell = f"**{_fmt_tok(med)}**"
    spread = p.get("spread_pct")
    if spread is not None and spread > 10:
        cell += f" (±{spread:.0f}%)"
    return cell


def _status_mark(res, delta_pct, threshold):
    marks = []
    if res.get("status") in ("failed", "partial"):
        marks.append("⚠" + res["status"])
    if delta_pct is not None and delta_pct < -threshold:
        marks.append(f"⚠{delta_pct:+.0f}% vs prev")
    if res.get("provenance") == "imported":
        marks.append("†")
    return " ".join(marks)


def collect(release):
    rows = {}
    for box_id, res in common.iter_results(release):
        rows[(res["recipe"], box_id)] = res
    return rows


def render_readme_block(release, prev_release, threshold):
    cur = collect(release)
    prev = collect(prev_release) if prev_release else {}
    lines = [
        BEGIN,
        "",
        f"Release **`{release}`** — one row per supported recipe "
        f"(`bench/recipes/`), measured by `bench/runner/bench.py`; "
        f"full report: [`docs/benchmarks/{release}.md`](docs/benchmarks/{release}.md). "
        "Single-stream decode and prefill are medians; batch is aggregate "
        "tok/s at the noted concurrency.",
        "",
        "| model | hardware | config | ctx | decode tok/s | batch | "
        "prefill 8K | needle | notes |",
        "|---|---|---|---:|---:|---:|---:|---|---|",
    ]
    for (recipe, box_id), res in sorted(cur.items()):
        model = recipe.split("/")[0]
        probes = res.get("probes") or {}
        hw, desc = _hw_and_desc(res)
        prev_res = prev.get((recipe, box_id))
        delta = None
        cur_med = (probes.get("decode") or {}).get("median_tok_s")
        prev_med = ((prev_res or {}).get("probes", {}).get("decode") or {}
                    ).get("median_tok_s")
        if cur_med and prev_med:
            delta = (cur_med - prev_med) / prev_med * 100
        notes = " ".join(x for x in (
            _spec_cell(res), _status_mark(res, delta, threshold)) if x)
        ctx = res.get("context")
        lines.append(
            f"| {model} | {hw} | {desc} | "
            f"{(str(ctx // 1024) + 'K') if ctx else '—'} | "
            f"{_decode_cell(res)} | {_batch_cell(probes.get('batch_decode'))} | "
            f"{_fmt_tok((probes.get('prefill') or {}).get('median_tok_s'))} | "
            f"{_needle_cell(probes.get('needle'))} | {notes} |")
    if any(r.get("provenance") == "imported" for r in cur.values()):
        lines += ["", "† imported from pre-harness measurements "
                      "(README/docs history) — re-measured on the next release."]
    lines += ["", END]
    return "\n".join(lines)


def render_report(release, prev_release, threshold):
    cur = collect(release)
    prev = collect(prev_release) if prev_release else {}
    out = [f"# Benchmark report — `{release}`", ""]
    out.append(f"Generated by `bench/runner/render.py` from "
               f"`bench/results/{release}/`. "
               + (f"Regression baseline: `{prev_release}`."
                  if prev_release else "No regression baseline."))
    regressions = []
    for (recipe, box_id), res in sorted(cur.items()):
        probes = res.get("probes") or {}
        out += ["", f"## {recipe} @ {box_id}", ""]
        out.append(f"- status: **{res.get('status', 'ok')}**, provenance: "
                   f"{res.get('provenance', 'live')}"
                   + (f", source: {res['source']}" if res.get("source") else ""))
        if res.get("summary"):
            out.append(f"- config: {res['summary']}"
                       + (f", context {res['context']}" if res.get("context") else ""))
        if res.get("load_time_s"):
            out.append(f"- load time: {res['load_time_s']:.0f} s")
        fp = res.get("env_fingerprint") or {}
        if fp:
            pkgs = fp.get("packages") or {}
            vt = fp.get("vllm_tree") or {}
            out.append(
                "- env: " + ", ".join(x for x in (
                    f"driver {fp.get('driver')}" if fp.get("driver") else "",
                    f"vllm {pkgs.get('vllm')}" if pkgs.get("vllm") else "",
                    f"tree {vt.get('sha')}{'+dirty' if vt.get('dirty') else ''}"
                    if vt.get("sha") else "",
                    f"torch {pkgs.get('torch')}" if pkgs.get("torch") else "",
                    f"patch {fp.get('patch_sha256')}" if fp.get("patch_sha256") else "",
                    f"cubins {fp.get('cubins')}" if fp.get("cubins") else "",
                ) if x))
        d = probes.get("decode") or {}
        if d.get("median_tok_s") is not None:
            extra = []
            if d.get("min_tok_s") is not None:
                extra.append(f"min {d['min_tok_s']}, max {d['max_tok_s']}, "
                             f"spread {d['spread_pct']}%")
            if d.get("distinct_outputs"):
                extra.append(f"distinct outputs {d['distinct_outputs']}")
            out.append(f"- decode: **{d['median_tok_s']} tok/s** median"
                       + (f" ({', '.join(extra)})" if extra else ""))
            prev_res = prev.get((recipe, box_id))
            pm = ((prev_res or {}).get("probes", {}).get("decode") or {}
                  ).get("median_tok_s")
            if pm:
                delta = (d["median_tok_s"] - pm) / pm * 100
                out.append(f"- vs `{prev_release}`: {delta:+.1f}%"
                           + (" ⚠ REGRESSION" if delta < -threshold else ""))
                if delta < -threshold:
                    regressions.append((recipe, box_id, delta))
        b = probes.get("batch_decode") or {}
        if b.get("levels"):
            cells = ", ".join(
                f"{n}→{lv['aggregate_tok_s']:g}"
                for n, lv in sorted(b["levels"].items(), key=lambda kv: int(kv[0])))
            out.append(f"- batch decode (aggregate): {cells}")
        p = probes.get("prefill") or {}
        if p.get("median_tok_s"):
            out.append(f"- prefill ({p.get('words', '?')} words): "
                       f"**{_fmt_tok(p['median_tok_s'])} tok/s** median")
        n = probes.get("needle") or {}
        if n.get("cases"):
            cells = ", ".join(
                (f"{(c.get('prompt_tokens') or 0) // 1000}K "
                 f"{'PASS' if c.get('pass') else 'FAIL'}")
                for c in n["cases"])
            out.append(f"- needle (depth {n['cases'][0].get('depth', '?')}): {cells}")
        a = probes.get("arithmetic") or {}
        if a.get("of"):
            out.append(f"- arithmetic: {a['score']}/{a['of']}")
        c = probes.get("coherence") or {}
        if c.get("of"):
            out.append(f"- coherence: {c['of'] - c['degenerate']}/{c['of']} clean")
        sm = res.get("server_metrics") or {}
        if sm.get("spec_acceptance_length"):
            out.append(f"- spec decode: acceptance length "
                       f"{sm['spec_acceptance_length']:g}"
                       + (f", draft acceptance {sm['draft_acceptance_pct']:g}%"
                          if sm.get("draft_acceptance_pct") else ""))
        pool = (res.get("server_metrics_final") or {}).get("pool_lines") or []
        for ln in pool[-2:]:
            out.append(f"- pool: `{ln}`")
        if res.get("serve_cmd"):
            out += ["", "<details><summary>serve command</summary>", "",
                    "```bash", res["serve_cmd"], "```", "", "</details>"]
        if res.get("notes"):
            out.append(f"- notes: {res['notes']}")
    if regressions:
        out += ["", "## ⚠ Regressions", ""]
        for recipe, box_id, delta in regressions:
            out.append(f"- {recipe} @ {box_id}: decode {delta:+.1f}%")
    out.append("")
    return "\n".join(out)


def splice_readme(block):
    with open(README) as f:
        text = f.read()
    if BEGIN in text and END in text:
        head, rest = text.split(BEGIN, 1)
        _, tail = rest.split(END, 1)
        return head + block + tail
    # first render: insert the section before "## Repository layout"
    anchor = "## Repository layout"
    section = f"## Benchmark results\n\n{block}\n\n"
    if anchor in text:
        return text.replace(anchor, section + anchor, 1)
    return text.rstrip() + "\n\n" + section


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--release", default=None)
    ap.add_argument("--check", action="store_true")
    args = ap.parse_args()

    mx = common.load_matrix()
    release = args.release or mx["current_release"]
    prev = mx.get("previous_release")
    threshold = mx.get("regression_threshold_pct", 5)

    if not any(True for _ in common.iter_results(release)):
        raise SystemExit(f"no results under bench/results/{release}/")

    block = render_readme_block(release, prev, threshold)
    new_readme = splice_readme(block)
    report = render_report(release, prev, threshold)
    report_path = os.path.join(common.REPO_DIR, "docs", "benchmarks",
                               f"{release}.md")

    if args.check:
        with open(README) as f:
            cur_readme = f.read()
        cur_report = ""
        if os.path.exists(report_path):
            with open(report_path) as f:
                cur_report = f.read()
        drift = []
        if cur_readme != new_readme:
            drift.append("README.md")
        if cur_report != report:
            drift.append(report_path)
        if drift:
            print("STALE (re-run bench/runner/render.py): " + ", ".join(drift))
            sys.exit(1)
        print("render check: up to date")
        return

    with open(README, "w") as f:
        f.write(new_readme)
    os.makedirs(os.path.dirname(report_path), exist_ok=True)
    with open(report_path, "w") as f:
        f.write(report)
    print(f"rendered README block + {report_path} for {release}")


if __name__ == "__main__":
    main()
